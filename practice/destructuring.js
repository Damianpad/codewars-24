// Example of Array destructuring

const favFoods = ["bacon", "pizza", "fries"];

const [ana, damian, lizzy] = favFoods;

console.log(ana);
console.log(damian);
console.log(lizzy);


// Example of an object destructuring

const person = {name: 'Damian', age: 29};

const { name, age } = person;

console.log(name);
console.log(age);